# GROUP 2: STRUCTURAL QUALITY — separation of concerns
# ══════════════════════════════════════════════════════════════════════════════

class TestStructuralQuality:
    """The refactored code must properly separate business logic from HTML rendering."""

    def test_has_multiple_functions_or_classes(self, refactored_ast):
        """Must define at least 3 top-level functions or classes (not just one giant blob)."""
        top_level = [
            node for node in ast.iter_child_nodes(refactored_ast)
            if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef))
        ]
        assert len(top_level) >= 3, (
            f"Expected at least 3 top-level functions/classes, found {len(top_level)}: "
            f"{[n.name for n in top_level]}. "
            f"The point is to SEPARATE concerns, not keep one big function."
        )

    def test_has_data_processing_layer(self, refactored_source):
        """Must have dedicated data-processing logic (function or class with 'process', 'calc', or 'data' in name)."""
        pattern = re.compile(
            r"(?:def|class)\s+((?:\w*(?:process|calc|data|compute|extract|prepare)\w*))",
            re.IGNORECASE,
        )
        matches = pattern.findall(refactored_source)
        assert matches, (
            "No data-processing function/class found. Expected at least one with "
            "'process', 'calc', 'data', 'compute', 'extract', or 'prepare' in its name. "
            "Business logic must be separated from HTML rendering."
        )

    def test_has_render_layer(self, refactored_source):
        """Must have dedicated rendering logic (function or class with 'render', 'html', or 'template' in name)."""
        pattern = re.compile(
            r"(?:def|class)\s+((?:\w*(?:render|html|template|format_html)\w*))",
            re.IGNORECASE,
        )
        matches = pattern.findall(refactored_source)
        assert matches, (
            "No rendering function/class found. Expected at least one with "
            "'render', 'html', or 'template' in its name."
        )

    def test_no_html_in_data_functions(self, refactored_source):
        """Data-processing functions must not contain HTML tags."""
        tree = ast.parse(refactored_source)
        source_lines = refactored_source.splitlines()

        data_keywords = {"process", "calc", "data", "compute", "extract", "prepare"}
        html_re = re.compile(r"<(?:div|span|h[1-6]|p|img|strong|style|body|html|head|meta)\b")

        for node in ast.walk(tree):
            if not isinstance(node, ast.FunctionDef):
                continue
            name_lower = node.name.lower()
            if not any(kw in name_lower for kw in data_keywords):
                continue

            # Check the function body for HTML strings
            func_lines = source_lines[node.lineno - 1 : node.end_lineno]
            func_text = "\n".join(func_lines)
            html_matches = html_re.findall(func_text)
            assert not html_matches, (
                f"Function '{node.name}' (data layer) contains HTML tags: {html_matches}. "
                f"Business logic must not generate HTML."
            )

    def test_generate_restaurant_page_is_thin(self, refactored_source):
        """The main orchestrator function should be short — it delegates, not implements."""
        tree = ast.parse(refactored_source)
        for node in ast.walk(tree):
            if isinstance(node, ast.FunctionDef) and node.name == "generate_restaurant_page":
                body_lines = node.end_lineno - node.lineno + 1
                assert body_lines <= 40, (
                    f"generate_restaurant_page is {body_lines} lines long. "
                    f"It should be a thin orchestrator (<= 40 lines) that calls "
                    f"data-processing and rendering functions. "
                    f"If it's still a monolith, you haven't refactored enough."
                )
                return
        pytest.fail("generate_restaurant_page() function not found in refactored_agent.py")

    def test_no_global_mutation(self, refactored_ast):
        """Refactored code should not use 'global' statements."""
        for node in ast.walk(refactored_ast):
            if isinstance(node, ast.Global):
                pytest.fail(
                    f"Found 'global {', '.join(node.names)}' at line {node.lineno}. "
                    f"The refactored version should not mutate global state."
                )



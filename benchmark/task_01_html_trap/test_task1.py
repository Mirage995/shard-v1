# GROUP 3: SAMPLE_MENU REUSE — must use the same data fixture
# ══════════════════════════════════════════════════════════════════════════════

class TestDataContract:
    """The refactored module must import and use SAMPLE_MENU from legacy_agent."""

    def test_sample_menu_importable(self, refactored_module):
        """The refactored module must make SAMPLE_MENU accessible."""
        # Either imports it from legacy_agent or re-exports it
        menu = getattr(refactored_module, "SAMPLE_MENU", None)
        if menu is None:
            pytest.fail(
                "SAMPLE_MENU not accessible on refactored_agent module. "
                "Import it from legacy_agent or define it identically."
            )
        assert menu == SAMPLE_MENU, "SAMPLE_MENU in refactored_agent differs from legacy_agent"

    def test_works_with_different_data(self, refactored_module):
        """Must work with a minimal alternative menu — not hardcoded to SAMPLE_MENU."""
        alt_menu = {
            "restaurant": {"name": "Test Cafe", "since": 2020, "city": "Roma", "motto": "Test"},
            "categories": [
                {
                    "name": "Bevande",
                    "items": [
                        {"name": "Espresso", "price": 1.20, "rating": 4.9,
                         "is_special": False, "allergens": []},
                    ],
                },
            ],
            "daily_special": None,
            "promo_code": "",
        }
        try:
            result = refactored_module.generate_restaurant_page(alt_menu)
        except Exception as e:
            pytest.fail(f"Refactored code crashed on alternative menu data: {e}")

        assert "Test Cafe" in result, "Alternative restaurant name not in output"
        assert "Espresso" in result, "Alternative menu item not in output"
        assert "\u20ac1.20" in result, "Alternative price not in output"



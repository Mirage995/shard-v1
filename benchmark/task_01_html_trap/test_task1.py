# GROUP 1: OUTPUT EQUIVALENCE — the HTML must be byte-identical
# ══════════════════════════════════════════════════════════════════════════════

class TestOutputEquivalence:
    """The refactored code must produce EXACTLY the same HTML as the legacy code."""

    def test_html_identical(self, reference_html, refactored_html):
        """Byte-for-byte comparison. No tolerance."""
        if reference_html == refactored_html:
            return  # perfect match

        # Find first difference and report it clearly
        ref_lines = reference_html.splitlines(keepends=True)
        new_lines = refactored_html.splitlines(keepends=True)

        for i, (ref_line, new_line) in enumerate(zip(ref_lines, new_lines), 1):
            if ref_line != new_line:
                pytest.fail(
                    f"HTML differs at line {i}:\n"
                    f"  EXPECTED: {ref_line.rstrip()!r}\n"
                    f"  GOT:      {new_line.rstrip()!r}"
                )

        if len(ref_lines) != len(new_lines):
            pytest.fail(
                f"HTML line count differs: expected {len(ref_lines)}, got {len(new_lines)}"
            )

    def test_html_length(self, reference_html, refactored_html):
        """Quick sanity: same character count."""
        assert len(refactored_html) == len(reference_html), (
            f"Length mismatch: expected {len(reference_html)}, got {len(refactored_html)} "
            f"(delta: {len(refactored_html) - len(reference_html):+d} chars)"
        )

    def test_all_css_classes_present(self, reference_html, refactored_html):
        """Every CSS class in the reference must appear in the refactored output."""
        class_re = re.compile(r'class="([^"]+)"')
        ref_classes = set()
        for match in class_re.finditer(reference_html):
            for cls in match.group(1).split():
                ref_classes.add(cls)

        new_classes = set()
        for match in class_re.finditer(refactored_html):
            for cls in match.group(1).split():
                new_classes.add(cls)

        missing = ref_classes - new_classes
        assert not missing, f"Missing CSS classes in refactored output: {missing}"

    def test_all_menu_items_present(self, refactored_html):
        """Every dish name from SAMPLE_MENU must appear in the output."""
        for cat in SAMPLE_MENU["categories"]:
            for item in cat["items"]:
                assert item["name"] in refactored_html, (
                    f"Menu item '{item['name']}' missing from refactored HTML output"
                )

    def test_all_prices_present(self, reference_html, refactored_html):
        """Every price string from the reference must appear in the refactored output."""
        price_re = re.compile(r"\u20ac\d+\.\d{2}")
        ref_prices = set(price_re.findall(reference_html))
        new_prices = set(price_re.findall(refactored_html))
        missing = ref_prices - new_prices
        assert not missing, f"Missing prices in refactored output: {missing}"

    def test_dalle_placeholders_present(self, reference_html, refactored_html):
        """All DALL-E image placeholders must be preserved."""
        img_re = re.compile(r'data-prompt="([^"]+)"')
        ref_prompts = set(img_re.findall(reference_html))
        new_prompts = set(img_re.findall(refactored_html))
        assert ref_prompts == new_prompts, (
            f"DALL-E prompts mismatch.\n"
            f"  Missing: {ref_prompts - new_prompts}\n"
            f"  Extra:   {new_prompts - ref_prompts}"
        )

    def test_promo_code_present(self, refactored_html):
        """Promo code from SAMPLE_MENU must appear."""
        assert SAMPLE_MENU["promo_code"] in refactored_html, (
            f"Promo code '{SAMPLE_MENU['promo_code']}' missing from output"
        )

    def test_daily_special_present(self, refactored_html):
        """Daily special dish name and pricing must appear."""
        special = SAMPLE_MENU["daily_special"]
        assert special["dish"] in refactored_html, (
            f"Daily special '{special['dish']}' missing from output"
        )

    def test_star_names_in_footer(self, refactored_html):
        """The footer must list star dish names (rating >= 4.7)."""
        star_names = []
        for cat in SAMPLE_MENU["categories"]:
            for item in cat["items"]:
                if item["rating"] >= 4.7:
                    star_names.append(item["name"])
        # They appear comma-joined in the stats footer
        expected = ", ".join(star_names)
        assert expected in refactored_html, (
            f"Star names '{expected}' not found in stats footer"
        )


